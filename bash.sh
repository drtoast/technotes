## COMMAND LINE HISTORY

# previous command: (ctrl-P or !!)
# next command: (ctrl-N)
# matching ANY part of search term: (ctrl-R or !? string)
# matching START of search term: (! search term)


# LINE NAVIGATION

# go to beginning/end: (ctrl-A, ctrl-E)
# go forward one word: (option-arrow, alt-F)
# go backward one word: (option-arrow, alt-B)


# LINE EDITING

# clear line before cursor: (ctrl-U)
# clear line after cursor: (ctrl-K)
# delete word before the cursor: (ctrl-W)
